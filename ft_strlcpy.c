#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t i;

	i = 0;
	if (size == 0)
		return (ft_strlen(src));
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = 0;
	return (ft_strlen(src));
}

int	main(void)
{
	char buffer[18] = "ASDSF";
	char str[] = "KAJSDHKJHSDKAH";
	printf("%d", (int )ft_strlcpy(buffer, str, 10));
}
